
public class MinMaxValues {
  Action action;
  
  Double value;

  
 public MinMaxValues(Action action, Double value) {
	super();
	this.action = action;
	this.value = value;
}
 
public Action getAction() {
	return action;
}



public void setAction(Action action) {
	this.action = action;
}



public void setValue(Double value) {
	this.value = value;
}



public double getValue() {
	return value;
}

public void setValue(double value) {
	this.value = value;
}

  
  
}
